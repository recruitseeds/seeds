function SidebarTrigger({
  className,
  onClick,
  ...props
}: React.ComponentProps<typeof Button>) {
  const { open, toggleSidebar } = useSidebar();
  const tooltipLabel = open ? "Close sidebar" : "Open sidebar";

  return (
    <Tooltip label={tooltipLabel} side="right">
      <Button
        data-sidebar="trigger"
        data-slot="sidebar-trigger"
        variant="ghost"
        size="icon"
        className={cn("h-7 w-7 rounded-md transition-colors", className)}
        onClick={(event) => {
          onClick?.(event);
          toggleSidebar();
        }}
        aria-label={tooltipLabel}
        {...props}
      >
        {open ? (
          <SidebarOpenIcon className="h-4 w-4" />
        ) : (
          <SidebarClosedIcon className="h-4 w-4" />
        )}
        <span className="sr-only">{tooltipLabel}</span>
      </Button>
    </Tooltip>
  );
}

